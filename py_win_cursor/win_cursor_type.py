class WinCursorType:
    """ This class defines constants for various types of Windows cursors.

    Each constant represents a specific system cursor type, which can be used
    to change the cursor appearance in a Windows environment.
    """
    IDC_ARROW = 32512  # Standard arrow cursor
    IDC_IBEAM = 32513  # I-beam cursor (used in text fields)
    IDC_WAIT = 32514   # Hourglass/wait cursor
    IDC_CROSS = 32515  # Crosshair cursor
    IDC_UPARROW = 32516  # Vertical arrow cursor
    IDC_SIZE = 32640  # Four-pointed arrow cursor sizing
    IDC_ICON = 32641  # Slashed circle cursor
    IDC_SIZENWSE = 32642  # Double-pointed arrow cursor (NorthWest-SouthEast)
    IDC_SIZENESW = 32643  # Double-pointed arrow cursor (NorthEast-SouthWest)
    IDC_SIZEWE = 32644  # Double-pointed arrow cursor (West-East)
    IDC_SIZENS = 32645  # Double-pointed arrow cursor (North-South)
    IDC_SIZEALL = 32646  # Four-pointed arrow cursor pointing North, South, East, and West
    IDC_NO = 32648  # Slashed circle cursor
    IDC_HAND = 32649  # Hand cursor
    IDC_APPSTARTING = 32650  # Arrow and hourglass cursor